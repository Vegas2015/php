<b>Key concepts:</b><br /><br>

<span style="font-family: inherit;">1.&nbsp; If two objects are moving in opposite directions towards each other &nbsp;at speeds u and v, then relative speed = Speed of first + Speed of second = u + v.<br />
This is also the speed at which they are moving towards each other or the speed at which they may be moving away from each other.</span><br />
<span style="font-family: inherit;"><br />
2. If the two objects move in the same direction with speeds u and v, then<br />
relative speed = difference of their speeds = u &#8211; v.&nbsp;</span><br />
<span style="font-family: inherit;"><br />
This is also the speed at which the faster object is either drawing closer to the slower object or moving away from the slower object as the case may be.</span><br />
<span style="font-family: inherit;"><br />
