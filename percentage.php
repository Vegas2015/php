<div dir="ltr" style="text-align: left;" trbidi="on"><div dir="ltr" style="text-align: left;" trbidi="on"><span style="font-family: inherit;">The problems on percentages may not appear directly but the applications are omni present in profit and loss, simple and compound interest and data interpretation and other areas.</span><br />
<span style="font-family: inherit;"><br />
Percent means for every hundred.&nbsp; $x\%&nbsp; = \displaystyle\frac{x}{{100}}$</span><br />
<span style="font-family: inherit;">Percentage conversions: You must commit to memory these percentage equivalents. </span><br />
<span style="font-family: inherit;">$1 = 100\% $; </span><br />
<span style="font-family: inherit;"><br />
$\displaystyle\frac{1}{2}$ = 50% ; </span><br />
<span style="font-family: inherit;"><br />
$\displaystyle\frac{1}{3}$ = 33<sup>1</sup>&#8260;<sub>3</sub>% or 33.33; </span><br />
<span style="font-family: inherit;"><br />
$\displaystyle\frac{1}{4}$ = 25% ;$\displaystyle\frac{3}{4}$ = 75% ;</span><br />
<span style="font-family: inherit;"><br />
$\displaystyle\frac{1}{5}$ = 20%; </span><br />
<span style="font-family: inherit;"><br />
$\displaystyle\frac{1}{6}$ = 16<sup>2</sup>&#8260;<sub>3</sub>% or 16.66%;</span><br />
<span style="font-family: inherit;"><br />
$\displaystyle\frac{1}{7}$ = 14<sup>2</sup>&#8260;<sub>7</sub>% or 14.28%; </span><br />
<span style="font-family: inherit;"><br />
$\displaystyle\frac{1}{8}$ = 12<sup>1</sup>&#8260;<sub>2</sub>% or 12.5%; </span><br />
<span style="font-family: inherit;"><br />
$\displaystyle\frac{1}{9}$ = 11<sup>1</sup>&#8260;<sub>9</sub>% or 11.11% ;</span><br />
<span style="font-family: inherit;"><br />
$\displaystyle\frac{1}{{10}}$ = 10%;</span><br />
<span style="font-family: inherit;"><br />
