<span style="font-family: inherit;"><br />
$\displaystyle\frac{1}{{12}}$ = 8<sup>1</sup>&#8260;<sub>3</sub>%;</span><br />
<span style="font-family: inherit;"><br />
<b><span style="color: #cc0000;">Formula 1:</span></b> </span><br />
<span style="font-family: inherit;">A is what percentage of B? $ \Rightarrow \displaystyle\frac{{\rm{A}}}{{\rm{B}}}{\rm{ \times 100}}$</span><br />
<span style="font-family: inherit;"><br />
<b><span style="color: #0b5394;">Formula 2:</span></b> </span><br />
<span style="font-family: inherit;">A is howmuch percent greater than B? $ \Rightarrow \displaystyle\frac{{{\rm{A - B}}}}{{\rm{B}}}{\rm{ \times 100}}$</span><br />
<span style="font-family: inherit;"><br />
<b><span style="color: #0b5394;">Formula 3:</span></b> </span><br />
<span style="font-family: inherit;">A is howmuch percent&nbsp;less than B? $ \Rightarrow \displaystyle\frac{{{\rm{B - A}}}}{{\rm{B}}}{\rm{ \times 100}}$</span><br />
<span style="font-family: inherit;"><br />
<b><span style="color: #0b5394;">Formula 4:</span></b> </span><br />
<span style="font-family: inherit;">If A is increased by K% then the new number is $(100 + K)\%&nbsp; \times A$</span><br />
<span style="font-family: inherit;">But there are several methods available to calculate easily. </span><br />
<span style="font-family: inherit;"><br />
Example: A is increased by 20% then the new number can be calculated as</span><br />
<span style="font-family: inherit;">Method 1: $(100 + 20)\%&nbsp; \times A \Rightarrow 120\%&nbsp; \times A$</span><br />
<span style="font-family: inherit;">Method 2: Calculate 20% of the given number and add to the original number A. $ \Rightarrow A + (20\%&nbsp; \times A)$</span><br />
<span style="font-family: inherit;">Method 3: If basic ratio is available for the given percentage then $ \Rightarrow A + (\displaystyle\frac{1}{5} \times A) \Rightarrow \frac{6}{5} \times A$</span><br />
<!-- adsense --><br />
<b><span style="color: #0b5394; font-family: inherit;">Formula 5:</span></b><br />
<span style="font-family: inherit;">If A is&nbsp;decreased by K% then the new number is $(100&nbsp;- K)\%  \times A$</span><br />
<span style="font-family: inherit;">But there are several methods available to calculate easily. </span><br />
<span style="font-family: inherit;"><br />
Example: A is&nbsp;decreased by 20% then the new number can be calculated as</span><br />
<span style="font-family: inherit;">Method 1: $(100&nbsp;- 20)\%  \times A \Rightarrow 80\%  \times A$</span><br />
<span style="font-family: inherit;">Method 2: Calculate 20% of the given number and substract from the original number A. $ \Rightarrow A&nbsp;- (20\%  \times A)$</span><br />
<span style="font-family: inherit;">Method 3: If basic ratio is available for the given percentage then $ \Rightarrow A&nbsp;- (\displaystyle\frac{1}{5} \times A) \Rightarrow \frac{4}{5} \times A$</span><br />
<span style="font-family: inherit;"><br />
<span style="color: #0b5394;"><b>Formula 6:</b></span> </span><br />
<span style="font-family: inherit;">$A\% (B) = B\% (A)$</span><br />
<span style="font-family: inherit;"><br />
<b><span style="color: #0b5394;">Formula 7:</span></b></span><br />
<span style="font-family: inherit;">If several percentages are acting on the same number then we can add all the percentages.</span><br />
<span style="font-family: inherit;">${x_1}\% (K) + {x_2}\% (K) + {x_3}\% (K)... = ({x_1} + {x_2} + {x_{3...}})\% (K)$</span><br />
<span style="font-family: inherit;"><br />
<span style="color: #0b5394;"><b>Formula 8:</b></span></span><br />
<span style="font-family: inherit;">If a number&nbsp;K got increased by A% and B% successively then the final percentage is given by $\left( {A + B + \displaystyle\frac{{AB}}{{100}}} \right)\% $</span><br />
<span style="font-family: inherit;"><span style="color: #cc0000;">Note1:</span> If decreased then substitute +A% with -A%</span><br />
