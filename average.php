  <h1>AVERAGE</h1>
  

<span style="font-family: inherit;">Assume 5 friends went to a movie.&nbsp; If the total money with the friends is equal to 1000 then we say that average money with each person is Rs.200/-&nbsp; But it is not necessary that each person has Rs.200.</span><br />
<span style="font-family: inherit;">Some may have more money than the others, but the total money is equal to Rs.1000.</span><br />
<span style="font-family: inherit;">Let us say Person E is rich and he bought Rs.500 to the movie.&nbsp; So the money with the remaining friends is equal to Rs.500.&nbsp; If E has not come to the movie, the average money with the other friends comes down to $\dfrac{{500}}{4}$ = Rs.125. &nbsp;If E has bought Rs.125 to the movie the average stands at Rs.125. &nbsp;But he has Rs.375 more than the average required. &nbsp;And this extra amount is distributed among all the friends equally, so that each person gets $\dfrac{{375}}{5} = 75$ extra. That is why final average = 125&nbsp;+ 75 = Rs.200.</span><br />
<span style="font-family: inherit;"><br />
</span> <b><span style="color: #cc0000; font-family: inherit;">Formula 1:</span></b><br />
<span style="font-family: inherit;">Average or mean: The Mean (Average) of a group of numbers is the sum of the numbers divided by the number of numbers:</span><br />
<span style="font-family: inherit;">Average or Mean = Sum of the observations / Number of observations</span><br />
<span style="font-family: inherit;"><br />
</span> <b><span style="color: #cc0000; font-family: inherit;">Formula 2:</span></b><br />
<span style="font-family: inherit;">If the average of &#8216;m &#8217; quantities is &#8216;x &#8216; and the average age of &#8216; n &#8216; other quantities is &#8216;y &#8216; then the average of all of them put together is = $\displaystyle\frac{{mx + ny}}{{m + n}}$</span><br />
<span style="font-family: inherit;"><br />
</span> <b><span style="color: #cc0000; font-family: inherit;">Formula 3:</span></b><br />
<span style="font-family: inherit;">If the average age of &#8216;m &#8216; quantities is &#8216;x &#8216; and the average age of &#8216;n &#8217; quantities out of them (m quantities) is &#8216; y &#8216; then the average of the rest of the quantities is = .$\displaystyle\frac{{mx - ny}}{{m - n}}$</span><br />
<span style="font-family: inherit;"><br />
</span> <b><span style="color: #cc0000; font-family: inherit;">Formula 4:</span></b><br />
<span style="font-family: inherit;">If the average of &#8216; n &#8216; numbers is &#8216; x &#8217; and if &#8216; k &#8216; is added to or subtracted from each given number the average of &#8216; n &#8216; numbers becomes (x+k) or (x-k) respectively. In the other words average value will be increased or decreased by &#8216; k &#8216;.</span><br />
<span style="font-family: inherit;"><br />
</span> <b><span style="color: #cc0000; font-family: inherit;">Formula 5:</span></b><br />
<span style="font-family: inherit;">If the average of &#8216; n &#8216;&nbsp; numbers is &#8216; x &#8216; and if each given number is multiplied to or divided by &#8216; k &#8216; then the average of n numbers becomes kx or&nbsp;$\displaystyle\frac{x}{k}$ respectively.</span><br />
<span style="font-family: inherit;"><br />
</span> <b><span style="color: #cc0000; font-family: inherit;">Formula 6:</span></b><br />
<span style="font-family: inherit;">If a person travels a distance at a speed of x km/hr and the same distance at a speed of y km/hr then the average speed during the whole journey is given by $\displaystyle\frac{{2xy}}{{x + y}}$ km/hr.</span><br />
<span style="font-family: inherit;">If a person covers A km at x km/hr and B km at y km/hr and C km at z km/hr, then the average speed in covering the whole distance is $\displaystyle\frac{{A + B + C}}{{\displaystyle\frac{A}{x} + \frac{B}{y} + \frac{C}{z}}}$km/hr.</span><br />
<span style="font-family: inherit;"><br />
</span> <b><span style="color: #cc0000; font-family: inherit;">Formula 7:</span></b><br />
<span style="font-family: inherit;">The average of n (where n is an odd number) consecutive numbers is always the middle number E.g. 1, 3, 5, 7, 9.</span><br />
<span style="font-family: inherit;">&nbsp;The Average = Middle number = 5</span><br />
<span style="font-family: inherit;"><br />
</span> <b><span style="color: #cc0000; font-family: inherit;">Formula 8:</span></b><br />
<span style="font-family: inherit;">The average &#8216; n &#8216; (where n is even number) consectuive numbers is the average of the two middle numbers. </span><br />
<span style="font-family: inherit;">E.g. Average of (2, 4, 6, 8, 10, 12) =&nbsp;$\displaystyle\frac{{6 + 8}}{2}$ = 7</span><br />
